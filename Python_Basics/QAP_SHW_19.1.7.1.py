# Условие: Вы работаете в крупной IT-компании в качестве QA-инженера.
# Ваша задача — разработать функцию-помощника для сотрудников отдела поддержки,
# которая должна формировать первичные ответ на простые вопросы.
#
# Функция должна принимать на вход список вопросов от пользователей и возвращать список ответов отдела поддержки.
# # Ответы на вопросы должны генерироваться автоматически, но в соответствии с ключевыми словами в вопросе.
# Например, если в вопросе есть слово «ошибка», ответом должно быть «Извините за причиненные неудобства.
# Наши специалисты уже работают над этой ошибкой».
#
# Ваша задача: только реализовать функцию customer_support_simulator, вызывать её не нужно.

def customer_support_simulator(questions):
   responses = []
   for question in questions:
       question = question.lower()
       if 'ошибка' in question:
           responses.append("Мы извиняемся за причиненные неудобства. Наши специалисты уже работают над этой ошибкой.")
       elif 'заказ' in question:
           responses.append("Ваш заказ обрабатывается. Мы уведомим вас, как только он будет отправлен.")
       elif 'вернуть' in question:
           responses.append("Вы можете вернуть товар в течение 14 дней с момента получения.")
       else:
           responses.append("Благодарим вас за обращение. Ваш вопрос передан специалистам.")
   return responses
